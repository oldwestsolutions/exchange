// Helper function to get company by symbol
export const getCompanyBySymbol = (symbol: string): CompanyInfo | undefined => {
  return companyDatabase.find(company => company.symbol === symbol);
};

// Helper function to get companies by sector
export const getCompaniesBySector = (sector: string): CompanyInfo[] => {
  return companyDatabase.filter(company => company.sector === sector);
};

// Helper function to get companies by risk level
export const getCompaniesByRiskLevel = (riskLevel: 'Low' | 'Medium' | 'High'): CompanyInfo[] => {
  return companyDatabase.filter(company => company.riskLevel === riskLevel);
};

// Helper function to search companies
export const searchCompanies = (query: string): CompanyInfo[] => {
  const lowercaseQuery = query.toLowerCase();
  return companyDatabase.filter(company => 
    company.symbol.toLowerCase().includes(lowercaseQuery) ||
    company.name.toLowerCase().includes(lowercaseQuery) ||
    company.sector.toLowerCase().includes(lowercaseQuery) ||
    company.industry.toLowerCase().includes(lowercaseQuery)
  );
};
